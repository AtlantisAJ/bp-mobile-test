// Экспортируем функцию initializeModal, чтобы ее можно было использовать в других модулях.
export function initializeModal() {
    // Находим модальное окно в DOM по его ID 'modal'.
    const modal = document.getElementById('modal');
    // Находим кнопку открытия модального окна по ее ID 'openModal'.
    const openModalBtn = document.getElementById('openModal');
    // Находим кнопку закрытия модального окна по ее ID 'closeModal'.
    const closeModalBtn = document.getElementById('closeModal');

    // Проверяем, найдены ли все три элемента (модальное окно, кнопка открытия, кнопка закрытия).
    // Это нужно, чтобы избежать ошибок, если какой-то элемент отсутствует на странице.
    if (openModalBtn && modal && closeModalBtn) {
        // Добавляем обработчик события 'click' на кнопку открытия модального окна.
        openModalBtn.addEventListener('click', () => {
            // При клике на кнопку добавляем класс 'show' к модальному окну.
            // CSS, связанный с этим классом, должен сделать окно видимым (например, display: block; opacity: 1;).
            modal.classList.add('show');
        });

        // Добавляем обработчик события 'click' на кнопку закрытия модального окна.
        closeModalBtn.addEventListener('click', () => {
            // При клике на кнопку удаляем класс 'show' у модального окна.
            // CSS, связанный с этим классом, должен скрыть окно (например, display: none; opacity: 0;).
            modal.classList.remove('show');
        });

        // Добавляем обработчик события 'click' непосредственно на элемент модального окна (обычно это фон/оверлей).
        modal.addEventListener('click', (event) => {
            // Проверяем, был ли клик сделан непосредственно по самому модальному окну (фону),
            // а не по его дочерним элементам (содержимому модального окна).
            if (event.target === modal) {
                // Если клик был по фону, удаляем класс 'show', чтобы закрыть модальное окно.
                // Это позволяет закрывать окно, кликая вне его основного контента.
                modal.classList.remove('show');
            }
        });
    } else {
        // Если один или несколько элементов не были найдены, выводим сообщение об ошибке в консоль.
        // Это помогает при отладке, указывая на возможную проблему в HTML-разметке или опечатку в ID.
        console.error('Modal elements not found!');
    }
}
